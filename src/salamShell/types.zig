const std = @import("std");
const utils = @import("../utils.zig");
pub const SshPacket = struct {
    packet_length: u32,
    padding_length: u8,
    payload: []const u8,
    mac: ?[]const u8 = null,

    pub fn format(self: SshPacket, w: *std.Io.Writer) !void {
        try w.print("{{ \n packet_length: {d}, \n padding_length: {d}, \n payload: {s}\n  mac: {s}\n  }}", .{ self.packet_length, self.padding_length, self.payload, utils.checkNullFormat(self.mac) });
        try w.flush();
    }
};

pub const NameList = struct {
    length: u32,
    names: []const []const u8,
    pub fn Parse(reader: *std.Io.Reader, alloc: std.mem.Allocator) !NameList {
        const log = std.log.scoped(.NameListParse);
        var name_list_length_array: [4]u8 = undefined;
        const read_bytes = try reader.readSliceShort(&name_list_length_array);
        std.debug.assert(read_bytes == 4);
        const name_list_length = std.mem.readInt(u32, &name_list_length_array, .big);
        log.info("name_list_length: {d}", .{name_list_length});
        const name_list = try reader.take(name_list_length);
        log.info("name_list: {s}", .{name_list});
        var names = try std.ArrayList([]const u8).initCapacity(alloc, 50);
        var it = std.mem.splitScalar(u8, name_list, ',');

        while (it.next()) |name| {
            try names.append(alloc, name);
        }
        return .{
            .length = name_list_length,
            .names = try names.toOwnedSlice(alloc),
        };
    }
    pub fn format(
        self: @This(),
        writer: *std.Io.Writer,
    ) std.Io.Writer.Error!void {
        try writer.print("[\n ", .{});
        for (self.names) |name| {
            try writer.print("\t{s},\n", .{name});
        }
        try writer.print("]\n", .{});
    }

    pub fn getFormatSendableNameList(self: NameList, alloc: std.mem.Allocator) ![]const u8 {
        var list = try std.ArrayList(u8).initCapacity(alloc, 1028);
        for (self.names) |value| {
            try list.print(alloc, "{s},", .{value});
        }
        // remove the last byte which is a ','
        const popped_comma = list.pop();
        std.debug.assert(popped_comma != null and popped_comma.? == ',');
        return try list.toOwnedSlice(alloc);
    }
};

